<!-- blog category show and show more card -->

<script>
  document.addEventListener("DOMContentLoaded", function () {
    const urlParams = new URLSearchParams(window.location.search);
    const selectedCategory = urlParams.get("category");

    const allCards = Array.from(document.querySelectorAll(".blog-card"));
    const tips = document.querySelectorAll(".blog-catg .blog-tip");
    const readMoreBtn = document.getElementById("readMoreBtn");
    const cardsPerPage = 6;
    let filteredCards = [];
    let visibleCount = 0;

    // Step 1: Highlight Active Category
    tips.forEach(tip => {
      tip.classList.remove("active");
      const tipCategory = tip.getAttribute("data-category");
      if (tipCategory === selectedCategory || (!selectedCategory && tipCategory === "All")) {
        tip.classList.add("active");
      }

      // Redirect to blog.php?category=...
      tip.addEventListener("click", () => {
        const category = tip.getAttribute("data-category");
        window.location.href = `blog.php?category=${encodeURIComponent(category)}`;
      });
    });

    // Step 2: Filter cards
    filteredCards = allCards.filter(card => {
      const cardCategory = card.getAttribute("data-category");
      if (!selectedCategory || selectedCategory === "All") return true;
      return cardCategory === selectedCategory;
    });

    // Step 3: Hide all cards first
    allCards.forEach(card => {
      card.style.display = "none";
    });

    // Step 4: Show first 6 filtered cards
    function showNextCards() {
      const nextCards = filteredCards.slice(visibleCount, visibleCount + cardsPerPage);
      nextCards.forEach(card => card.style.display = "block");
      visibleCount += nextCards.length;

      if (visibleCount >= filteredCards.length) {
        readMoreBtn.style.display = "none";
      }
    }

    showNextCards(); // show initial 6

    // Step 5: Read More button logic
    readMoreBtn.addEventListener("click", showNextCards);

    // Step 6: Hide button if no more cards
    if (filteredCards.length <= cardsPerPage) {
      readMoreBtn.style.display = "none";
    }
  });
</script>





